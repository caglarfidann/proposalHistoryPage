package com.gwt.kyu.shared;

public class ProcessDetail {
	private String SiraNo;
	private String KullaniciKodu;
	private String BaslangicTarih;
	private String BitisTarih;
	private String Süre;
	private String Sonuc;
	
	public String getSiraNo() {
		return SiraNo;
	}
	public void setSiraNo(String siraNo) {
		SiraNo = siraNo;
	}
	public String getKullaniciKodu() {
		return KullaniciKodu;
	}
	public void setKullaniciKodu(String kullaniciKodu) {
		KullaniciKodu = kullaniciKodu;
	}
	public String getBaslangicTarih() {
		return BaslangicTarih;
	}
	public void setBaslangicTarih(String baslangicTarih) {
		BaslangicTarih = baslangicTarih;
	}
	public String getBitisTarih() {
		return BitisTarih;
	}
	public void setBitisTarih(String bitisTarih) {
		BitisTarih = bitisTarih;
	}
	public String getSüre() {
		return Süre;
	}
	public void setSüre(String süre) {
		Süre = süre;
	}
	public String getSonuc() {
		return Sonuc;
	}
	public void setSonuc(String sonuc) {
		Sonuc = sonuc;
	}
	
}
